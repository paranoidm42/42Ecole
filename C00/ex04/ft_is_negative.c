#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_is_negative(int n)
{
	char	x;

	x = 'P';
	if (n < 0)
	{
		x = 'N';
	}	
	ft_putchar(x);
}
